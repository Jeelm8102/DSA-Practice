#include <bits/stdc++.h>
using namespace std;
int finally_internship, roz, savar, sanj, divas, raat, happiness;

void mahenat(int roz) {}

void mahenat(int savar, int sanj) {}
void mahenat(int divas, int raat) {}

int main()
{
  internship();
}




int internship()
{
  while (finally_internship == false)
  {
    mahenat(roz);
    mahenat(savar, sanj);
    mahenat(divas, raat);
  }
  return happiness;
}
